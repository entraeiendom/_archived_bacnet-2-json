    private String propertyIdentifierHex;

    public static PropertyIdentifier fromPropertyIdentifierHex(String hexString) {
        for (PropertyIdentifier type : values()) {
            if (type.getPropertyIdentifierHex().equals(hexString)) {
                return type;
            }
        }
        return null;
    }


    public static PropertyIdentifier fromOctet(Octet propertyIdentifierOctet) throws NumberFormatException {
        if (propertyIdentifierOctet == null) {
            return null;
        }
        return fromPropertyIdentifierHex(propertyIdentifierOctet.toString());
    }


    public String getPropertyIdentifierHex() {
        return propertyIdentifierHex;
    }

    private PropertyIdentifier(String hexString) {
        this.propertyIdentifierHex = hexString;
    }
